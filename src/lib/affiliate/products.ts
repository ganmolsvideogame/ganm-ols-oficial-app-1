export function getAffiliateProducts() {
  return AFFILIATE_PRODUCTS;
}

export function getHomeAffiliateProducts() {
  return AFFILIATE_PRODUCTS.filter((product) => product.showOnHome).sort(
    (left, right) => {
      if (left.isFeatured !== right.isFeatured) {
        return left.isFeatured ? -1 : 1;
      }

      return (
        new Date(right.publishedAt).getTime() - new Date(left.publishedAt).getTime()
      );
    }
  );
}

export function getAffiliateProductBySlug(slug: string) {
  return AFFILIATE_PRODUCTS.find((product) => product.slug === slug) ?? null;
}

export function buildAffiliateProductPath(slug: string) {
  return `/parceiros/${encodeURIComponent(slug)}`;
}

export function buildAffiliateCheckoutPath(slug: string, source?: string | null) {
  const basePath = `${buildAffiliateProductPath(slug)}/comprar`;
  const cleanSource = String(source ?? "").trim();

  if (!cleanSource) {
    return basePath;
  }

  const params = new URLSearchParams({ source: cleanSource });
  return `${basePath}?${params.toString()}`;
}

export function buildAffiliateRecommendationPath(
  slug: string,
  source?: string | null,
  fromSlug?: string | null
) {
  const basePath = `${buildAffiliateProductPath(slug)}/abrir`;
  const params = new URLSearchParams();
  const cleanSource = String(source ?? "").trim();
  const cleanFromSlug = String(fromSlug ?? "").trim();

  if (cleanSource) {
    params.set("source", cleanSource);
  }

  if (cleanFromSlug) {
    params.set("from", cleanFromSlug);
  }

  const query = params.toString();
  return query ? `${basePath}?${query}` : basePath;
}

export function buildAffiliateProductAbsoluteUrl(slug: string) {
  return buildAbsoluteUrl(buildAffiliateProductPath(slug));
}
